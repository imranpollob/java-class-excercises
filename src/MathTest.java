public class MathTest {
    /**
     * Test some functionalities of Math library
     */
    public static void test() {
        System.out.println("Sin of 100: " + Math.sin(100));
        System.out.println("50 degree in radian is: " + Math.toRadians(50));
        System.out.println("atan of 100: " + Math.atan(100));
        System.out.println("power of 4 over 5: " + Math.pow(4, 5));
        System.out.println("Sqrt of 64 is: " + Math.sqrt(64));
        System.out.println("Sqrt of -64 is: " + Math.sqrt(-64));
        System.out.println("Ceil of 10.5: " + Math.ceil(10.5));
        System.out.println("Floor of 10.5: " + Math.floor(10.5));
        System.out.println("Round of 10.5: " + Math.round(10.5));
        System.out.println("Round of 10.4: " + Math.round(10.4));
        System.out.println("Minimum of 10 and 5 is: " + Math.min(10, 5));
        System.out.println("A random number between 0 and 9: "+ (int) (Math.random() * 10));
        System.out.println("A random number between 50 and 100: "+ (int) (50 + Math.random() * 51));
        System.out.println("A random number between 50 and 100: "+ (int) (50 + Math.random() * 51));
        System.out.println("A random number between 50 and 100: "+ (int) (50 + Math.random() * 51));
        System.out.println("A random number between 50 and 100: "+ (int) (50 + Math.random() * 51));
    }
}
